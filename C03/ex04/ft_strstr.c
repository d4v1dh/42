char		*ft_strstr(char *str, char *to_find)
{
	int i;
	int h;

	i = 0;
	if (to_find[0] == '\0')
	{
		return (str);
	}
	while (str[i] != '\0')
	{
		h = 0;
		while (to_find[h] == str[i + h])
		{
			if (to_find[h + 1] == '\0')
			{
				return (&str[i]);
			}
			h++;
		}
		i++;
	}
	return (0);
}
