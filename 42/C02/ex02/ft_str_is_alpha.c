int			ft_str_is_alpha(char *str)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A' && str[i] <= 'Z') || (str[i] >= 'a'
					&& str[i] <= 'z'))
		{
			b++;
		}
		i++;
	}
	if (i == b || str[0] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
