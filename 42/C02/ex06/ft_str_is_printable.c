int			ft_str_is_printable(char *str)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			b++;
		}
		i++;
	}
	if (i == b || str[0] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
